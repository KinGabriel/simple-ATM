namespace ATM_Application;

public class User
{
   private string userName;
   private string password;
   private double amount;

   public string UserName
   {
      get { return userName; }
      set { userName = value; }
   }

   public string Password
   {
      get { return password; }
      set { password = value; }
   }

   public double Amount
   {
      get { return amount; }
      set { amount = value; }
   }
   
   
   
}